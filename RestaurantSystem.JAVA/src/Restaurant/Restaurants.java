    private void jBtnTotal3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnTotal3MouseClicked
        double cTotal1 = Double.parseDouble(jlblCostOfDrinks.getText());
        double cTotal2 = Double.parseDouble(jlblCostOfMeal.getText());
        double cTotal3 = Double.parseDouble(jlblDeliveryCost.getText());
        double cTotal4 = Double.parseDouble(jtxtTax.getText());
        
        //this is a subtotal variable
        double subTotal = (cTotal1+cTotal2+cTotal3);
        //this is a GrandTotal Variable
        double allTotal = (cTotal1+cTotal2+cTotal3+cTotal4);
        
        String SUBTotal = String.format("KSH %.2f", subTotal);
        String ALLTotal = String.format("KSH %.2f", allTotal);

        jtxtGrandTotal.setText(ALLTotal);
        jtxtSubtotal.setText(SUBTotal);
    }//GEN-LAST:event_jBtnTotal3MouseClicked

